
public class SimTSInputReaderException extends Exception {

	private static final long serialVersionUID = -1374296052283931205L;

	public SimTSInputReaderException() {
		super();
	}

	public SimTSInputReaderException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public SimTSInputReaderException(String arg0) {
		super(arg0);
	}

	public SimTSInputReaderException(Throwable arg0) {
		super(arg0);
	}
	
}
